a=sadsadsa.sfsfds.jpg

echo ${a#*.} #  '#'是运算符 *. 表示删除左边以.开头的字符串,留下右边字符串 []
echo ${a##*.} # '#'是运算符 *. 表示删除左边以.开头的所有字符串,留下右边字符串

echo ${a%.*} #'#'是运算符 *. 表示删除右边以.开头的字符串,留下右边字符串
echo ${a%%.*} #'#'是运算符 *. 表示删除右边以.开头的所有字符串,留下右边字符串

echo ${a:0:3} # 从左边第几个位置开始取几个字符 这里是从0这个位置向后取三个字符
echo ${a:7}   #从左边第八个字符开始向右截取
echo ${a:0-7} #从右边开始第七个字符开始向向右截取

